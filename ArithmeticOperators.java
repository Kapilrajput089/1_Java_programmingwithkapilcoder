public class ArithmeticOperators{
    public static void main(String[] args) {
        //// Arithmetic operators are :-(+,-,/,*,%).
        int a=10,b=30,c;
        c=a+b;
        System.out.println("Addition of A + B : = "+c);
        c=a-b;
        System.out.println("Subtraction of A - B : = "+c);
        c=a/b;
        System.out.println("Division of A / B : = "+c);
        c=a*b;
        System.out.println("Multiplication of A * B : = "+c);
        c=a%b;
        System.out.println("Moduls of A % B : = "+c);
        //These all are Arithetic operators in java programming.
    
    }   
}